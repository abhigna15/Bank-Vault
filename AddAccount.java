    private void OKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OKActionPerformed
       
        Connection conn;
        MyConnection addacc1 = new MyConnection();
        conn = addacc1.getConnection();
        String querys = "insert into customer1(cno,cname,caddress,cph,cbalance) values(?,?,?,?,?)";
        if(accnofield.getText().isEmpty() || usnfield.getText().isEmpty() || addressfield.getText().isEmpty() || phnofield.getText().isEmpty() ||  balfield.getText().isEmpty() ){
                JOptionPane.showMessageDialog(rootPane,"Please fill in the required information\n Account not created");
            }else{
        try{
            PreparedStatement pstmt = conn.prepareStatement(querys);
            
            pstmt.setString(1,accnofield.getText());
            pstmt.setString(2,usnfield.getText());
            pstmt.setString(3,addressfield.getText());
            pstmt.setString(4,phnofield.getText());
            pstmt.setString(5, balfield.getText());
            
            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(rootPane,"Account Created Successfully with the account number  "+accnofield.getText());
            }
        catch(SQLException ex){
            Logger.getLogger(AddAccount.class.getName()).log(Level.SEVERE,null,ex);
        }
        }
    }//GEN-LAST:event_OKActionPerformed

