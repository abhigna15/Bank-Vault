    private void accnofieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_accnofieldKeyTyped
        char c = evt.getKeyChar();
        if(!(Character.isDigit(c)) || (c == KeyEvent.VK_BACK_SPACE) || (c == KeyEvent.VK_DELETE) || (accnofield.getText()+evt.getKeyChar()).length()>2){
                evt.consume();
        }
     
        
    }//GEN-LAST:event_accnofieldKeyTyped

    private void usnfieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_usnfieldKeyPressed
        char c = evt.getKeyChar();
      
        if(Character.isLetter(c) || Character.isWhitespace(c) || Character.isISOControl(c)){
            usnfield.setEditable(true);
        }else{
            usnfield.setEditable(false);
        }
    }//GEN-LAST:event_usnfieldKeyPressed

    private void phnofieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_phnofieldKeyPressed
        String phoneNumber = phnofield.getText();
        int length = phoneNumber.length();
        
        char c = evt.getKeyChar();
        
        if(evt.getKeyChar()>='0'&& evt.getKeyChar()<='9'){
            if(length<10){
                phnofield.setEditable(true);
            }
            else{
                phnofield.setEditable(false);
        }
        }
            else{
                    if(evt.getExtendedKeyCode()==KeyEvent.VK_BACK_SPACE ||evt.getExtendedKeyCode()==KeyEvent.VK_DELETE ){
                        phnofield.setEditable(true);
                    }else{
                        usnfield.setEditable(false);
                    }
                    }
        
    }//GEN-LAST:event_phnofieldKeyPressed

